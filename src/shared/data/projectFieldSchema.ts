export function getFieldsBySection(section: string): ProjectFieldSchema[] {
  return PROJECT_FIELD_SCHEMA.filter((field) => field.section === section);
}

export function getFieldsByStage(stage: string): ProjectFieldSchema[] {
  return PROJECT_FIELD_SCHEMA.filter((field) => field.stages.includes(stage));
}

export function getFieldsByArea(area: string): ProjectFieldSchema[] {
  return PROJECT_FIELD_SCHEMA.filter((field) => field.impactedAreas.includes(area));
}

export function getRequiredFieldsByStage(stage: string): ProjectFieldSchema[] {
  return PROJECT_FIELD_SCHEMA.filter((field) => field.required && field.stages.includes(stage));
}

export function getFieldByKey(key: string): ProjectFieldSchema | undefined {
  return PROJECT_FIELD_SCHEMA.find((field) => field.key === key);
}

export function getSections(): string[] {
  return [...new Set(PROJECT_FIELD_SCHEMA.map((field) => field.section))];
}
